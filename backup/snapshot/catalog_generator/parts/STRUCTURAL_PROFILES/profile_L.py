class profileLCustom(geom.prim.ShapeObject):
    def __init__(
        self,
        s,
        B: float,
        H: float,
        t_f: float,
        height: float,
        r1: float = 0.0,
        r2: float = 0.0,
        position: PositionL = PositionL.CENTER,
    ):
        self.H = H
        self.B = B
        self.t_f = t_f
        if r1 == 0.0:
            self.r1 = t_f
        else:
            self.r1 = r1
        if r2 == 0.0:
            self.r2 = t_f / 2
        else:
            self.r2 = r2
        self.height = height
        o1 = geom.build_L_shape(
            s, self.B, self.H, self.t_f, self.r1, self.r2, self.height
        )
        if position != PositionL.CENTER:
            translate_args = position.get_translation_custom(
                self.B, self.H, self.t_f, self.r1, self.r2
            )
            if translate_args:
                o1.move(**translate_args)

        super().__init__(o1.obj)

